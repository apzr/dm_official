	/**
	 * 得到日期字符串 默认格式（yyyy-MM-dd） pattern可以为："yyyy-MM-dd" "HH:mm:ss" "E"
	 */
	public static String formatDate(Date date, Object... pattern) {
		String formatDate = null;
		if (pattern != null && pattern.length > 0) {
			formatDate = DateUtils.format(date, pattern[0].toString());
		} else {
			formatDate = DateUtils.format(date, "yyyy-MM-dd HH:mm:ss");
		}
		return formatDate;
	}

	private static String format(Date date, String string) {
		String str = "";
		DateFormat sdf = new SimpleDateFormat(string);   
        try {   
            //方法一   
            str = sdf.format(date);   
        } catch (Exception e) {   
            e.printStackTrace();   
        }
		
		return str;  
	}

